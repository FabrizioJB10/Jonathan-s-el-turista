        <p>Conocida como la ciudad de los brujos, debido a la intensa atmósfera esotérica presente, siendo hogar de un gran número de chamanes, curanderos y adivinos, quienes normalmente atienden a personas que acuden de distintos rincones del país a buscar respuesta a sus males. Entre sus sitios más famosos, destaca el Mercado de los Brujos, un curioso mercado que se instala cerca de la plaza de armas, donde es posible adquirir todo tipo de ingredientes para la elaboración de pócimas y medicinas alternativas.</p>
    </div>
    <div class="caja">
        <h3>7.IQUITOS-Playas de Iquitos</h3>
