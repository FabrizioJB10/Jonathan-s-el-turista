        <p>Puerta de entrada a la extraordinaria belleza de la selva amazónica, el bosque tropical más extenso del planeta, declarado el 2011 como una de las siete maravillas naturales del mundo. Desde la ciudad se inician la mayoría de las rutas de exploración que permiten internarse por sus bosques vírgenes, ya sea navegando por el caudaloso Río Amazonas o a través de caminatas por sus impresionantes reservas.</p>
    </div>
    <div class="caja">
